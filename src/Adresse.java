
public class Adresse {
String rue;
String voie;
public String getRue() {
	return rue;
}
public void setRue(String rue) {
	this.rue = rue;
}
public String getVoie() {
	return voie;
}
public void setVoie(String voie) {
	this.voie = voie;
}
public Adresse(String rue, String voie) {
	super();
	this.rue = rue;
	this.voie = voie;
}

}
